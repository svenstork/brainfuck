use std::{
    fs::File,
    io::{Error, Write},
};

use super::super::commands::config::Config;

pub(crate) fn dump_memory(config: Config, memory: &Vec<u8>) -> Result<(), Error> {
    if let Some(mem_dump) = config.memory_dump {
        let mut file = File::create(mem_dump)?;
        file.write_all(memory)?;
    }
    Ok(())
}
